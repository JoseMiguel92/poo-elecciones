    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // BOTON PARA GUARDAR HISTÓRICO AL DISCO DURO
        if(historico==null||historico.size()<1){
            JOptionPane.showMessageDialog(acciones,
                "Para poder guardar el histórico tienes que crear una elección primero.",
                "No hay elecciones",
            JOptionPane.ERROR_MESSAGE);
            salidaTexto.append("\nGuardado cancelado: No se puede guardar, no hay al menos una elección creada.");
            return;
        }
        JFileChooser chooser=new JFileChooser();
        int opcion = chooser.showSaveDialog(acciones);
        if(opcion!=JFileChooser.APPROVE_OPTION){
            salidaTexto.append("\nGuardado cancelado por el usuario");
            return;
        } 
        try{
            salidaTexto.append("\nGuardando como archivo binario: "+chooser.getSelectedFile());
            try (ObjectOutputStream salida = new ObjectOutputStream(new FileOutputStream(chooser.getSelectedFile()))) {
                salida.writeObject(historico);
            }
            salidaTexto.append("\nGuardado completado");
            
        } catch (IOException e) {
            salidaTexto.append("\nHa ocurrido un error al intentar guardar el archivo. Prueba a seleccionar otra carpeta");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButtonSimularEleccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimularEleccionActionPerformed

        int seleccion = jTableHistorico.getSelectedRow();
        if(seleccion==-1){
            JOptionPane.showMessageDialog(acciones,
                "No has marcado ninguna elección.",
                "No hay elección",
            JOptionPane.ERROR_MESSAGE);
            return;
        }
        int opcion = JOptionPane.showConfirmDialog(acciones, "¿Deseas introducir los votos a mano?");
        // Si elige si, los votos los pedirá a mano, si cancela o cualquier otra cosa, los votos serán calculados aleatoriamente
        votosManuales = (opcion==JOptionPane.OK_OPTION)?true:false;
        eleccion = historico.get(seleccion);
        eleccion.realizarEleccion(votosManuales);
        int totalVotos= 0 ;
        for(ItemVotos x1 : eleccion.getResultadosTotalVotos()){
            totalVotos+=x1.getNumeroVotos();
        }
        DefaultTableModel muestraPartidos = (DefaultTableModel) jTableMuestraPartidos.getModel();
        muestraPartidos.setRowCount(0);
        for (int i = 0; i < eleccion.getResultadosTotalEscaños().size(); i++) {
            FormacionPolitica partido = eleccion.getResultadosTotalEscaños().get(i).getFormacion();
            muestraPartidos.addRow(new Object[]{
                partido.getNombre(),
                eleccion.getResultadosTotalVotos().get(i).getNumeroVotos(),
                (eleccion.getResultadosTotalVotos().get(i).getNumeroVotos()*100)/totalVotos,
                eleccion.getResultadosTotalEscaños().get(i).getNumeroEscaños()
            });
        }
    }//GEN-LAST:event_jButtonSimularEleccionActionPerformed

